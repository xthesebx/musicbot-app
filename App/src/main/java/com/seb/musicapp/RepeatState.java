package com.seb.musicapp;

/**
 * <p>RepeatState class.</p>
 *
 * @author xXTheSebXx
 * @version 1.0-SNAPSHOT
 */
public enum RepeatState {
    /**
     * State for no repeating
     */
    NO_REPEAT,
    /**
     * State to repeat the queue
     */
    REPEAT_QUEUE,
    /**
     * State to repeat single song
     */
    REPEAT_SINGLE;
}
